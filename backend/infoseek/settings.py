# Database configuration from environment variables
DATABASE_URL = os.getenv('DATABASE_URL', 'postgresql://postgres:postgres@localhost:5432/infoseek')

# Parse DATABASE_URL
if DATABASE_URL.startswith('postgresql://'):
    # Format: postgresql://user:password@host:port/dbname
    db_url = DATABASE_URL.replace('postgresql://', '')
    if '@' in db_url:
        auth, rest = db_url.split('@', 1)
        if ':' in auth:
            user, password = auth.split(':', 1)
        else:
            user, password = auth, ''
        
        if '/' in rest:
            host_port, dbname = rest.rsplit('/', 1)
            if ':' in host_port:
                host, port = host_port.split(':', 1)
            else:
                host, port = host_port, '5432'
        else:
            host, port, dbname = rest, '5432', 'infoseek'
    else:
        user, password, host, port, dbname = 'postgres', 'postgres', 'localhost', '5432', 'infoseek'
else:
    user, password, host, port, dbname = 'postgres', 'postgres', 'localhost', '5432', 'infoseek'

